def xiu1(str):
    str = re.sub("\d|\.", "", str)
    return str[str.find("：")+1::]


def xiu2(str):
    str = re.search(r'\：[\u4e00-\u9fa5]{0,}', str).group()
    return str[1::]


f = open("1.md", "a", encoding="utf-8")
