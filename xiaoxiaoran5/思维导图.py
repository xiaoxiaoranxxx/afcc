for i in str.strip().split("\n"):
    if("章" in i):
        print("## " + xiu1(i))
        f.write("## " + xiu1(i)+"\n")
    elif("知识体" in i):
        print("### "+xiu2(i))
        f.write("### "+xiu2(i)+"\n")

    else:
        print("#### "+xiu1(i))
        f.write("#### "+xiu1(i)+"\n")
f.close
